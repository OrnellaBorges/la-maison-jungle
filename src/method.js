
// .map 

/* méthode JavaScript map() passe sur chaque élément d'un tableau.
Elle lui applique une fonction, et renvoie un nouveau tableau 
contenant les résultats de cette fonction appliquée sur chaque élément.
*/

const numbers = [1, 2, 3, 4]
const doubles = numbers.map(number => number * 2) // [2, 4, 6, 8]